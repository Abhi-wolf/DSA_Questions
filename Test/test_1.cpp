class Solution{
  public:
    // Should return head of the modified linked list
    Node *sortedInsert(struct Node* head, int data) {
        // Code here
        Node *t=new Node(data);
        Node *p=head;
        Node *q=NULL;
        
        if(head == NULL)
        {
            //t->next=head;
            head=t;
        }
        else
        {
            while(p->data < data && p!=NULL)
            {
                q=p;
                p=p->next;
            }
            
            if(p==head)
            {
                t->next=head;
                head=t;
            }
            else
            {
                t->next=q->next;   
                q->next=t;
            }
        }
        return head;
    }
};


